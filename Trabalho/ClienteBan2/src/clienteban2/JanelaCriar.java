    private void bCriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCriaActionPerformed
        // TODO add your handling code here:
        StringBuilder ordem = new StringBuilder();
        StringBuilder valores = new StringBuilder();
        if (tabela.hasInsertionFunction()) {
            valores.append("SELECT ");
            valores.append(tabela.getInsertionFunctionName());
            valores.append('(');

        } else {
            valores.append("INSERT INTO ");
            valores.append(tabela.getName());
            valores.append(" VALUES (");
        }
        boolean isFirst = true;

        for (Map.Entry<Coluna, JTextField> entry : this.campos.entrySet()) {
            Coluna coluna = entry.getKey();
            JTextField text = entry.getValue();

            if (!coluna.isValueValid(text.getText())) {
                JOptionPane.showMessageDialog(this, "Por favor, verifique o campo " + coluna.getNome() + "!", "Valores incorretos", JOptionPane.ERROR_MESSAGE);
                text.requestFocus();
                return;
            }

            //ordem.append(coluna.getNome());
            //ordem.append(",");
            if (!isFirst) {
                valores.append(", ");
            } else {
                isFirst = false;
            }

            if (coluna.getTipo().equals("string")) {
                valores.append('\'');
            }

            valores.append(text.getText());
            
            if (coluna.getTipo().equals("string")) {
                valores.append('\'');
            }

        }
        valores.append(");");
        String toDo = valores.toString();
        System.out.println(toDo);
        try {
            ClienteBan2.getInstance().conexao.RequestChange(toDo);
            JOptionPane.showMessageDialog(this, "Dados inseridos com sucesso!");
            janelaPrincipal.setupTabela();
            this.campos.values().forEach((text) -> {
                text.setText("");
            });
        } catch (Exception ex) {
            Logger.getLogger(JanelaCriar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bCriaActionPerformed


