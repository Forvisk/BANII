    private void setupInput() {
        for (int i = 0; i < tabela.getColunas().size(); i++) {

            Coluna coluna = tabela.getColunas().get(i);

            JLabel label = new JLabel(coluna.getNome().toUpperCase() + String.join("", Collections.nCopies(5, " ")) + "( " + coluna.getTipo() + (coluna.isPrimaryKey() ? ", Primary Key " : "") + " ):");
            label.setMaximumSize(new Dimension(Integer.MAX_VALUE, 10));
            label.setMinimumSize(new Dimension(50, 50));
            pDados.add(label);

            JTextField text = new JTextField("");

            //text.setEditable(!coluna.isPrimaryKey());
            text.setMaximumSize(new Dimension(Integer.MAX_VALUE, 10));
            text.setMinimumSize(new Dimension(50, 50));
            pDados.add(text);

            this.campos.put(coluna, text);

            //pDados.add(panel);
        }
    }

