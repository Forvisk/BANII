    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            TipoQuarto tq = Gerenciador.getInstancia().getTipoDeQuartos().get(jComboBox1.getSelectedIndex() + 1);
            System.out.println("Tipo " + tq.getNome());

            Quarto quarto = new Quarto(myHotel.getQuartos().size() + 1, jTextField1.getText(), myHotel, tq, tq.getPrecos().get(myHotel));

            myHotel.addQuarto(quarto);

            Gerenciador.getInstancia().addQuarto(quarto);

            ConnectDB.getInstance().RequestChange(quarto.insertQuery());
            jTextField1.setText("");
            JOptionPane.showMessageDialog(this, "Quarto no hotel " + myHotel.getNome() + " criado com sucesso!", "Quarto criado!", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage(), "Falha ao criar quarto!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

