    private void getHotel() {
        String nome = cHotel.getSelectedItem().toString();
        for (Hotel h : Gerenciador.getInstancia().getHoteis().values()) {
            if (h.getNome().equals(nome)) {
                hotel = h;
                break;
            }
        }
    }

    private void getQuarto() {
        String nome = cQuarto.getSelectedItem().toString();
        for (Quarto h : hotel.getQuartos().values()) {
            if (h.getLocal().equals(nome)) {
                quarto = h;
                break;
            }
        }
    }

    private void atualizaQuarto() {
        for (Quarto q : hotel.getQuartos().values()) {
            cQuarto.addItem(q.getLocal());
        }
    }

    private void atualizaFunc() {
        for (Funcionario f : hotel.getFuncionarios().values()) {
            cFunc.addItem(f.getNome());
        }
    }

    private void atualizaEstadia() {
        for (Estadia e : Gerenciador.getInstancia().getEstadias().values()) {
            if (e.getQuarto() == quarto) {
                cQuarto.addItem(e.getCodigo() + " com check in de " + e.getCheckIn());
            }
        }
    }

